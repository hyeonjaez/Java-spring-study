package com.nhnacademy.jaehyeon;

public class Number {

    private final int number;
    private final int count;

    public Number(int number, int count) {
        this.number = number;
        this.count = count;
    }

    public int getNumber() {
        return number;
    }

    public int getCount() {
        return count;
    }
}
